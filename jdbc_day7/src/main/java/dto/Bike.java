package dto;

public class Bike {
	private int bikeId;
	
	private String bikeName ;
	private String bikeBrand;
	private double bikeCC;
	private double bikePrice;
	private String bikeColour;
	public int getBikeId() {
		return bikeId;
	}
	public void setBikeId(int bikeId) {
		this.bikeId = bikeId;
	}
	public String getBikeName() {
		return bikeName;
	}
	public void setBikeName(String bikeName) {
		this.bikeName = bikeName;
	}
	public String getBikeBrand() {
		return bikeBrand;
	}
	public void setBikeBrand(String bikeBrand) {
		this.bikeBrand = bikeBrand;
	}
	public double getBikeCC() {
		return bikeCC;
	}
	public void setBikeCC(double bikeCC) {
		this.bikeCC = bikeCC;
	}
	public double getBikePrice() {
		return bikePrice;
	}
	public void setBikePrice(double bikePrice) {
		this.bikePrice = bikePrice;
	}
	public String getBikeColour() {
		return bikeColour;
	}
	public void setBikeColour(String bikeColour) {
		this.bikeColour = bikeColour;
	}
	

}
